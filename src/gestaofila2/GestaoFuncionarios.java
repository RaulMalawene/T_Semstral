    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
         try {
            // TODO add your handling code here:
            FileWriter open = new FileWriter("Funcionarios.txt", true);
            BufferedWriter write = new BufferedWriter(open);
          
            if(nomeCompleto.getText().isEmpty() && idade.getText().isEmpty() && usuarioFuncionario.getText().isEmpty() &&
               senha.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "PREENCHA OS ESPACOS");
                    
                }else{
                      write.write(nomeCompleto.getText()+";"+idade.getText()+";"+sexos+";"+
                      usuarioFuncionario.getText()+";"+senha.getText());  
                      write.newLine();
                      write.close();
                      open.close();
            }
        } catch (IOException ex) {
            Logger.getLogger(GestaoFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void tabela123MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela123MouseMoved
        // TODO add your handling code here:
           try {
           FileReader open = new FileReader("Funcionarios.txt");
           BufferedReader chamar = new BufferedReader(open);
           
           String primeiraLinha = chamar.readLine().trim();
           String[] colunas = primeiraLinha.split(";");
           
           DefaultTableModel model = (DefaultTableModel)tabela123.getModel();
           model.setColumnIdentifiers(colunas);
           
          Object[] linhaTabela =chamar.lines().toArray();
          
          for(int x = 0; x <linhaTabela.length; x++){
             
              String linha = linhaTabela[x].toString().trim();
              String[] linhadados = linha.split(";");
              model.addRow(linhadados);
          }
             
        } catch (Exception e) {
            
        } 
    }//GEN-LAST:event_tabela123MouseMoved

